package com.example.engine.internal;

/**
* Point represents a position of a Hexagon
* */
public class Point {

    int i;
    int j;

    public Point(int i, int j) {
        this.i = i;
        this.j = j;
    }

    public int getI() {
        return i;
    }

    public int getJ() {
        return j;
    }
}
